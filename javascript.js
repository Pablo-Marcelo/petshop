/*-------------escondendo e mostrando div. botão premier--------------*/

var premier = document.querySelector("#logo-marca-premier")

var premierpd = document.querySelector("#premier-pd")

premier.addEventListener('click', function(){
    
    if(premierpd.style.display === 'block'){
        premierpd.style.display = 'none'
        
    }else{

        premierpd.style.display = 'block'
        formulapd.style.display = 'none'
        goldenpd.style.display = 'none'
        mustpd.style.display = 'none'
        tutanopd.style.display = 'none'
        quatreepd.style.display = 'none'

    }
   
})


